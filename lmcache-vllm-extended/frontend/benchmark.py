CSV_FIELDS = [
    "request_id",
    "context_id",
    "question",
    "token_length",
    "context_tokens",
    "ttft_s",
    "total_latency_s",
    "response_length",
    "response_preview",
    "batch_id",
    "batch_size",
    "original_position",
    "scheduled_position",
    "scheduler_strategy",
]


def save_results(results: List[dict], output_path: str):
    """Write benchmark results to a CSV file."""
    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
    with open(output_path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=CSV_FIELDS)
        writer.writeheader()
        writer.writerows(results)
    print(f"\nResults saved to {output_path}")


def graph_q1(csv_path: str, output_path: str):
    """Plot token_length vs ttft and save the graph."""
    output_dir = os.path.dirname(output_path) or "."
    points = []

    with open(csv_path, "r", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            token_length = row.get("token_length")
            ttft = row.get("ttft_s")
            if not token_length or not ttft:
                continue
            try:
                points.append((int(token_length), float(ttft)))
            except ValueError:
                continue

    if not points:
        print("No valid Q1 data found to plot.")
        return

    # Sort by token length so the line plot is monotonic in x.
    points.sort(key=lambda item: item[0])
    token_lengths, ttft = zip(*points)

    plt.figure(figsize=(8, 5))
    plt.plot(token_lengths, ttft, marker="o", linestyle="-")
    plt.scatter(token_lengths, ttft, s=16, alpha=0.6)
    plt.xlabel("token_length")
    plt.ylabel("ttft_s")
    plt.title("Q1: token_length vs ttft_s")
    plt.grid(True, alpha=0.3)

    os.makedirs(output_dir, exist_ok=True)
    plt.savefig(output_path, bbox_inches="tight")
    plt.close()
    print(f"Q1 graph saved to {output_path}")


# -----------------------------------------------------------------------
# CLI entry-point
# -----------------------------------------------------------------------
def main():
    parser = argparse.ArgumentParser(
        description="Task 1/2 Benchmark — measure LLM inference latency & throughput",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples:\n"
            "  python benchmark.py --mode random --num-requests 30\n"
            "  python benchmark.py --mode random --batch-size 8 --scheduler none\n"
            "  python benchmark.py --mode random --batch-size 8 --scheduler grouped\n"
        ),
    )

    # Server settings
    parser.add_argument("--ip", default=IP, help="vLLM server IP")
    parser.add_argument("--port", type=int, default=PORT, help="vLLM server port")

    # Request generation
    parser.add_argument("--data-dir", default="data/", help="Context files folder")
    parser.add_argument("--seed", type=int, default=42, help="Random seed")
    parser.add_argument(
        "--mode",
        choices=[
            "random",
            "sequential",
            "repeated",
            "random-extended",
            "increasing_length",
            "same_context_same_question",
            "same_context_multiple_questions",
        ],
        default="random",
        help=(
            "random: shuffled contexts (Q3 diversity). "
            "sequential: grouped by context (Q1 token-length). "
            "same_context_same_question: Exact same prompt repeated (Max cache hit). "
            "same_context_multiple_questions: One context, variety of questions. "
            "repeated: same context and request repeated (Q2 cache reuse). Call same context and same question 5 times, then another question 5 times, etc. Afterwards, call another context with the same question 5 times, then another question 5 times, etc. "
            "increasing_length: fixed 10 requests with increasing prompt length across distinct contexts."
        ),
    )
    # specific numbers for reqeusts, questions, contexts, etc
    parser.add_argument("--num-requests", type=int, default=30, help="Number of requests (random/repeated)")
    parser.add_argument("--num-questions", type=int, default=5, help="Questions per context")
    parser.add_argument("--num-contexts", type=int, default=3, help="Number of contexts for 'repeated' mode")
    parser.add_argument("--num-per-context", type=int, default=3, help="Requests per context (sequential)")
    parser.add_argument(
        "--batch-size",
        type=int,
        default=1,
        help="Task 2 batch size. Default 1 keeps the original Task 1 path.",
    )
    parser.add_argument(
        "--scheduler",
        choices=VALID_STRATEGIES,
        default="none",
        help=(
            "Task 2 scheduler strategy. "
            "'none' preserves incoming order; 'grouped' groups by context_id."
        ),
    )
    parser.add_argument(
        "--batch-timeout-ms",
        type=int,
        default=50,
        help="Maximum backend queue wait for a partial Task 2 batch.",
    )

    # specific contexts or questions
    parser.add_argument("--context-id", default=None, help="Context ID for 'repeated' mode")
    parser.add_argument("--question", default=None, help="Specific question text to use")

    # Output
    parser.add_argument(
        "-o", "--output",
        default=None,
        help="Output CSV path (default: results/<mode>_<timestamp>.csv)",
    )
    parser.add_argument(
        "-g", "--graph-dir",
        default=None,
        help="Output folder for Q1 graph (uses q1_graph.png filename)",
    )

    args = parser.parse_args()
    if args.batch_size < 1:
        parser.error("--batch-size must be at least 1")
    if args.batch_timeout_ms < 1:
        parser.error("--batch-timeout-ms must be at least 1")

    # --- Generate requests ---
    gen = RequestGenerator(data_dir=args.data_dir, seed=args.seed)
    print(f"Loaded {len(gen.contexts)} contexts: {gen.get_context_ids()}")

    if args.mode == "random":
        requests = gen.generate(args.num_requests)
    elif args.mode == "random-extended":
        requests = gen.generate_extended(args.num_requests)
    elif args.mode == "sequential":
        requests = gen.generate_sequential(num_per_context=args.num_per_context)

    elif args.mode == "increasing_length":
        requests = gen.generate_increasing_length()

    elif args.mode == "same_context_same_question":
        ctx = args.context_id or gen.get_context_ids()[0]
        requests = gen.generate_same_context_same_question(
            context_id=ctx, 
            question=args.question, 
            num_requests=args.num_requests
        )

    elif args.mode == "same_context_multiple_questions":
        ctx = args.context_id or gen.get_context_ids()[0]
        requests = gen.generate_same_context_multiple_questions(
            context_id=ctx, 
            num_questions=args.num_questions
        )

    elif args.mode == "repeated":
        # New repeated logic: Different contexts, multiple questions each
        # If context_id is provided as a single string, we wrap it in a list
        ctx_list = [args.context_id] if args.context_id else None
        requests = gen.generate_multiple_contexts_multiple_questions(
            num_contexts=args.num_contexts,
            context_ids=ctx_list
        )
    print(f"Generated {len(requests)} requests in '{args.mode}' mode\n")

    # --- Run benchmark ---
    use_batch_mode = args.batch_size > 1 or args.scheduler != "none"
    if use_batch_mode:
        batches = gen.generate_batches(requests, args.batch_size)
        print(
            f"Task 2 batch mode enabled: {len(batches)} batches, "
            f"batch_size={args.batch_size}, scheduler={args.scheduler}\n"
        )
        results = run_benchmark_batches(
            batches,
            scheduler_strategy=args.scheduler,
            ip=args.ip,
            port=args.port,
            batch_timeout_ms=args.batch_timeout_ms,
        )
    else:
        results = run_benchmark(requests, ip=args.ip, port=args.port)

    # --- Save results ---
    if args.output:
        out_path = args.output
        save_results(results, out_path)
        if args.graph_dir:
            graph_path = args.graph_dir
            if not graph_path.lower().endswith(".png"):
                graph_path = os.path.join(graph_path, "q1_graph.png")
            graph_q1(out_path, graph_path)
        else:
            graph_q1(out_path, "results/q1_graph.png")
    elif args.mode == "increasing_length":
        print("Skipping CSV output for increasing_length (use -o to save).")
        if args.graph_dir:
            print("Skipping graph output because no CSV was saved.")
    else:
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        if use_batch_mode:
            out_path = (
                f"results/{args.mode}_batch{args.batch_size}_"
                f"{args.scheduler}_{timestamp}.csv"
            )
        else:
            out_path = f"results/{args.mode}_{timestamp}.csv"
        save_results(results, out_path)
        if args.graph_dir:
            graph_path = args.graph_dir
            if not graph_path.lower().endswith(".png"):
                graph_path = os.path.join(graph_path, "q1_graph.png")
            graph_q1(out_path, graph_path)
        else:
            graph_q1(out_path, "results/q1_graph.png")


if __name__ == "__main__":
    main()
