"""
Benchmark runner for Task 1 — Baseline Evaluation

Sends requests generated by RequestGenerator to the vLLM server,
measures latency (time-to-first-token + total response time), and
computes throughput.  Results are saved to CSV for plotting.

Usage:
    # Random requests (Q3 — diversity analysis)
    python benchmark.py --mode random --num-requests 30

    # Sequential / grouped by context (Q1 — token length vs latency)
    python benchmark.py --mode sequential --num-per-context 3

    # Repeated same context (Q2 — cache reuse)
    python benchmark.py --mode repeated --context-id vllm --num-requests 10

    # Specify output file
    python benchmark.py --mode random --num-requests 50 -o results/random_run.csv
"""

import os
import sys
import csv
import time
import argparse
from datetime import datetime
from typing import List

import chat_session
import matplotlib.pyplot as plt
from request_generator import RequestGenerator, Request


# -----------------------------------------------------------------------
# Defaults
# -----------------------------------------------------------------------
IP = "127.0.0.1"
PORT = 8000
SYSTEM_PROMPT = (
    "You are a helpful assistant. I will now give you a document and "
    "please answer my question afterwards based on the content in document"
)
CONTEXT_SEPARATOR = "###"


# -----------------------------------------------------------------------
# Single-request execution
# -----------------------------------------------------------------------
def run_single_request(
    ip: str,
    port: int,
    request: Request,
    system_prompt: str = SYSTEM_PROMPT,
) -> dict:
    """
    Send one request to the vLLM server and measure timing.

    Returns a dict with all measured fields.
    """
    # Create a fresh ChatSession per request so conversation history
    # doesn't accumulate and skew results.
    # Session creation + context setting happen OUTSIDE the timing block
    # so we only measure actual inference latency.
    session = chat_session.ChatSession(ip, port)
    session.set_context([system_prompt, request.context_text])

    # --- Timing ---
    ttft = None  # time to first token
    token_chunks = []
    total_start = time.perf_counter()

    for chunk in session.chat(request.question):
        # chat_session.chat() yields a trailing "(Response delay: ...)" string
        # after the real tokens — skip it so it doesn't pollute measurements.
        if chunk.startswith("\n\n(Response delay:"):
            continue
        if ttft is None:
            ttft = time.perf_counter() - total_start
        token_chunks.append(chunk)

    total_latency = time.perf_counter() - total_start

    response_text = "".join(token_chunks)

    return {
        "request_id": request.request_id,
        "context_id": request.context_id,
        "question": request.question,
        "token_length": request.token_length,
        "context_tokens": request.context_tokens,
        "ttft_s": round(ttft, 4) if ttft else None,
        "total_latency_s": round(total_latency, 4),
        "response_length": len(response_text),
        "response_preview": response_text[:120].replace("\n", " "),
    }


# -----------------------------------------------------------------------
# Full benchmark run
# -----------------------------------------------------------------------
def run_benchmark(
    requests: List[Request],
    ip: str = IP,
    port: int = PORT,
    system_prompt: str = SYSTEM_PROMPT,
    verbose: bool = True,
) -> List[dict]:
    """Run all requests sequentially, returning a list of result dicts."""

    results = []
    total_start = time.perf_counter()

    for i, req in enumerate(requests):
        if verbose:
            print(
                f"[{i + 1}/{len(requests)}]  ctx={req.context_id:<30}  "
                f"q=\"{req.question[:50]}...\"",
                flush=True,
            )

        try:
            result = run_single_request(ip, port, req, system_prompt)
            results.append(result)

            if verbose:
                print(
                    f"          TTFT={result['ttft_s']:.3f}s  "
                    f"Total={result['total_latency_s']:.3f}s  "
                    f"Tokens={result['token_length']}"
                )
        except Exception as e:
            print(f"  ERROR on request {req.request_id}: {e}", file=sys.stderr)
            results.append({
                "request_id": req.request_id,
                "context_id": req.context_id,
                "question": req.question,
                "token_length": req.token_length,
                "context_tokens": req.context_tokens,
                "ttft_s": None,
                "total_latency_s": None,
                "response_length": 0,
                "response_preview": f"ERROR: {e}",
            })

    wall_time = time.perf_counter() - total_start
    successful = [r for r in results if r["total_latency_s"] is not None]

    if verbose:
        print("\n" + "=" * 60)
        print(f"BENCHMARK COMPLETE")
        print(f"  Total requests:  {len(requests)}")
        print(f"  Successful:      {len(successful)}")
        print(f"  Wall-clock time: {wall_time:.2f}s")
        if successful:
            avg_latency = sum(r["total_latency_s"] for r in successful) / len(successful)
            ttft_entries = [r["ttft_s"] for r in successful if r["ttft_s"]]
            avg_ttft = sum(ttft_entries) / len(ttft_entries) if ttft_entries else 0
            throughput = len(successful) / wall_time
            print(f"  Avg latency:     {avg_latency:.3f}s")
            print(f"  Avg TTFT:        {avg_ttft:.3f}s")
            print(f"  Throughput:      {throughput:.2f} req/s")
        print("=" * 60)

    return results


# -----------------------------------------------------------------------
# CSV output
# -----------------------------------------------------------------------
CSV_FIELDS = [
    "request_id",
    "context_id",
    "question",
    "token_length",
    "context_tokens",
    "ttft_s",
    "total_latency_s",
    "response_length",
    "response_preview",
]


def save_results(results: List[dict], output_path: str):
    """Write benchmark results to a CSV file."""
    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
    with open(output_path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=CSV_FIELDS)
        writer.writeheader()
        writer.writerows(results)
    print(f"\nResults saved to {output_path}")


def graph_q1(csv_path: str, output_path: str):
    """Plot token_length vs total_latency_s and save the graph."""
    points = []

    with open(csv_path, "r", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            token_length = row.get("token_length")
            total_latency = row.get("total_latency_s")
            if not token_length or not total_latency:
                continue
            try:
                points.append((int(token_length), float(total_latency)))
            except ValueError:
                continue

    if not points:
        print("No valid Q1 data found to plot.")
        return

    # Sort by token length so the line plot is monotonic in x.
    points.sort(key=lambda item: item[0])
    token_lengths, total_latencies = zip(*points)

    plt.figure(figsize=(8, 5))
    plt.plot(token_lengths, total_latencies, marker="o", linestyle="-")
    plt.scatter(token_lengths, total_latencies, s=16, alpha=0.6)
    plt.xlabel("token_length")
    plt.ylabel("total_latency_s")
    plt.title("Q1: token_length vs total_latency_s")
    plt.grid(True, alpha=0.3)

    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
    plt.savefig(output_path, bbox_inches="tight")
    plt.close()
    print(f"Q1 graph saved to {output_path}")


# -----------------------------------------------------------------------
# CLI entry-point
# -----------------------------------------------------------------------
def main():
    parser = argparse.ArgumentParser(
        description="Task 1 Benchmark — measure LLM inference latency & throughput"
    )

    # Server settings
    parser.add_argument("--ip", default=IP, help="vLLM server IP")
    parser.add_argument("--port", type=int, default=PORT, help="vLLM server port")

    # Request generation
    parser.add_argument("--data-dir", default="data/", help="Context files folder")
    parser.add_argument("--seed", type=int, default=42, help="Random seed")
    parser.add_argument(
        "--mode",
        choices=["random", "sequential", "repeated"],
        default="random",
        help=(
            "random: shuffled contexts (Q3 diversity). "
            "sequential: grouped by context (Q1 token-length). "
            "repeated: same context repeated (Q2 cache reuse)."
        ),
    )
    parser.add_argument("--num-requests", type=int, default=30, help="Number of requests (random/repeated)")
    parser.add_argument("--num-per-context", type=int, default=3, help="Requests per context (sequential)")
    parser.add_argument("--context-id", default=None, help="Context ID for 'repeated' mode")

    # Output
    parser.add_argument(
        "-o", "--output",
        default=None,
        help="Output CSV path (default: results/<mode>_<timestamp>.csv)",
    )

    args = parser.parse_args()

    # --- Generate requests ---
    gen = RequestGenerator(data_dir=args.data_dir, seed=args.seed)
    print(f"Loaded {len(gen.contexts)} contexts: {gen.get_context_ids()}")

    if args.mode == "random":
        requests = gen.generate(args.num_requests)
    elif args.mode == "sequential":
        requests = gen.generate_sequential(num_per_context=args.num_per_context)
    elif args.mode == "repeated":
        ctx = args.context_id or gen.get_context_ids()[0]
        requests = gen.generate_repeated(ctx, args.num_requests)

    print(f"Generated {len(requests)} requests in '{args.mode}' mode\n")

    # --- Run benchmark ---
    results = run_benchmark(requests, ip=args.ip, port=args.port)

    # --- Save results ---
    if args.output:
        out_path = args.output
    else:
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        out_path = f"results/{args.mode}_{timestamp}.csv"

    save_results(results, out_path)
    graph_q1(out_path, "results/q1_graph.png")


if __name__ == "__main__":
    main()
